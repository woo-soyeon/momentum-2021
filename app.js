const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  multi: function (a, b) {
    console.log(a * b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  powerOf: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(30, 5);
calculator.minus(30, 5);
calculator.multi(30, 5);
calculator.divide(30, 5);
calculator.powerOf(2, 3);
